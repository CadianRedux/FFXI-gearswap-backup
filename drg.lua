 -- WS function --
function precast(spell)
	if spell.english == "Impulse Drive" then
		equip(sets.WS)
	end

	if spell.english == "Camlann's Torment" then
		equip(sets.WS)
	end

	if spell.english == "Stardiver" then
		equip(sets.WS.SDiver)
	end

	if spell.english == "Jump" then
		equip(sets.JA.Jump)
	end

	if spell.english == "High Jump" then
		equip(sets.JA.HiJump)
	end

	if spell.english == "Spirit Jump" then
		equip(sets.JA.SpJump)
	end

	if spell.english == "Soul Jump" then
		equip(sets.JA.SoJump)
	end

	if spell.english == "SpiritLink" then
		equip(sets.JA.SpiritLink)
	end

	if spell.english == "Breath" then
		equip(sets.Breath)
	end

	if spell.english == "SpiritSurge" then
		equip(sets.JA.SpiritSurge)
	end
	end
-- End WS function --

 -- Equip set when finished casting --
function aftercast(spell)
	if player.status == "Engaged" then
		equip(sets.TP)
	end
	if player.status == "Idle" then equip(sets.aftercast.DT)
	end
end
 -- End aftercast function --

 -- Function to switch to TP when engaging --
function status_change(new,old)
	if new == 'Engaged' then
		equip(sets.TP)
	end
  function status_change(new,old)
  	if new == 'Idle' then
  		equip(sets.aftercast.DT)
  	end
  end
end
 -- End engaged function --
